package com.loopy.app.blocks

/**
 * 블록의 문법 어휘 — 모양 · 홈 · 분류.
 *
 * 모양이 문법을 강제한다. 모자 블록은 위가 둥글어 아무것도 붙일 수 없고, 캡 블록은 아래가
 * 평평해 뒤에 이어붙일 수 없다. 규칙을 문서로 설명하는 대신 손으로 만져 알게 하는 방식이다.
 *
 * 색은 기능을 말한다. 같은 색이 모여 있으면 같은 종류의 일을 한다는 뜻이고, 색이 바뀌는
 * 지점이 곧 흐름이 바뀌는 지점이다.
 *
 * 여기에는 어휘(enum)만 둔다. 어떤 블록이 어떤 모양인지는 [BlockDef] 가 정한다 —
 * 정의가 두 곳에 있으면 새 블록마다 두 번 적어야 하고, 그게 곧 부채가 된다.
 */

/** 블록의 결합 규칙. */
enum class BlockShape {
    /** 위가 둥글어 아무것도 붙일 수 없다. 스크립트는 여기서 시작한다. */
    HAT,

    /** 위 오목 / 아래 볼록. 위아래로 물린다. */
    STACK,

    /** 안에 블록을 품는다. 반복과 조건. */
    C_BLOCK,

    /** 아래가 평평해 뒤에 이어붙일 수 없다. 실행을 끝낸다. */
    CAP,

    /** 둥근 값 블록(리포터). 다른 블록의 값 홈에 꽂힌다. 센서값·변수읽기·연산. */
    REPORTER,

    /** 육각 참/거짓 블록. 조건 홈(육각)에만 꽂힌다. 비교·논리. */
    HEXAGON,
}

/**
 * 입력 홈.
 *
 * 스크래치에서 모양이 문법을 강제하는 이유가 여기 있다. 둥근 홈에는 값만, 육각 홈에는
 * 참/거짓만 들어간다. 잘못된 것을 넣으려 해도 모양이 맞지 않아 애초에 들어가지 않는다.
 * 문법 오류라는 개념 자체가 사라진다.
 */
enum class SlotKind {
    NONE,

    /** (값) — 숫자나 글자. 둥근 홈. */
    VALUE,

    /** <조건> — 참/거짓. 육각 홈. */
    BOOLEAN,
}

/** 기능 분류. 팔레트의 탭이자 색의 근거. */
enum class BlockCategory(val label: String) {
    EVENT("이벤트"),
    ACTION("동작"),
    CONTROL("제어"),
    SYSTEM("시스템"),
    DATA("변수"),
}
